import 'json_parser.dart';

/// Generates Repository classes with cache-first logic
///
/// Naming: DataProductRepository (follows user's convention)
class RepositoryGenerator {
  /// Generate Repository abstract interface
  String generateRepositoryInterface(String entityName) {
    final buffer = StringBuffer();

    buffer.writeln('// Generated by Zuraffa on ${DateTime.now().toIso8601String()}');
    buffer.writeln('// DO NOT EDIT - Regenerate with: zuraffa generate $entityName');
    buffer.writeln();

    buffer.writeln("import 'package:zuraffa/zuraffa.dart';");
    buffer.writeln("import '../entities/${_toSnakeCase(entityName)}.dart';");
    buffer.writeln();

    buffer.writeln('/// Abstract Repository for $entityName');
    buffer.writeln('abstract class ${entityName}Repository {');
    buffer.writeln('  Future<Result<$entityName, AppFailure>> getById(String id);');
    buffer.writeln('  Future<Result<List<$entityName>, AppFailure>> getAll();');
    buffer.writeln('  Future<Result<$entityName, AppFailure>> create($entityName entity);');
    buffer.writeln('  Future<Result<$entityName, AppFailure>> update($entityName entity);');
    buffer.writeln('  Future<Result<void, AppFailure>> delete(String id);');
    buffer.writeln('}');

    return buffer.toString();
  }

  /// Generate Repository implementation with cache-first logic
  String generateRepositoryImplementation(String entityName) {
    final className = 'Data${entityName}Repository';
    final buffer = StringBuffer();

    buffer.writeln('// Generated by Zuraffa on ${DateTime.now().toIso8601String()}');
    buffer.writeln('// DO NOT EDIT - Regenerate with: zuraffa generate $entityName');
    buffer.writeln();

    buffer.writeln("import 'package:zuraffa/zuraffa.dart';");
    buffer.writeln("import '../../domain/entities/${_toSnakeCase(entityName)}.dart';");
    buffer.writeln("import '../../domain/repositories/${_toSnakeCase(entityName)}_repository.dart';");
    buffer.writeln("import '../datasources/${_toSnakeCase(entityName)}_datasource.dart';");
    buffer.writeln();

    buffer.writeln('/// Implementation of ${entityName}Repository');
    buffer.writeln('/// ');
    buffer.writeln('/// Cache-first strategy:');
    buffer.writeln('/// 1. Try remote datasource');
    buffer.writeln('/// 2. Cache result locally');
    buffer.writeln('/// 3. On network failure, return cached data');
    buffer.writeln('class $className implements ${entityName}Repository {');
    buffer.writeln('  final ${entityName}DataSource remoteDataSource;');
    buffer.writeln('  final ${entityName}DataSource localDataSource;');
    buffer.writeln();
    buffer.writeln('  $className({');
    buffer.writeln('    required this.remoteDataSource,');
    buffer.writeln('    required this.localDataSource,');
    buffer.writeln('  });');
    buffer.writeln();

    // getById with cache-first
    buffer.writeln('  @override');
    buffer.writeln('  Future<Result<$entityName, AppFailure>> getById(String id) async {');
    buffer.writeln('    try {');
    buffer.writeln('      // Try remote first');
    buffer.writeln('      final remote = await remoteDataSource.getById(id);');
    buffer.writeln('      // Cache it');
    buffer.writeln('      await localDataSource.update(remote);');
    buffer.writeln('      return Success(remote);');
    buffer.writeln('    } catch (e) {');
    buffer.writeln('      // Network failed, try cache');
    buffer.writeln('      try {');
    buffer.writeln('        final cached = await localDataSource.getById(id);');
    buffer.writeln('        return Success(cached);');
    buffer.writeln('      } catch (cacheError) {');
    buffer.writeln('        return Failure(NetworkFailure(e.toString()));');
    buffer.writeln('      }');
    buffer.writeln('    }');
    buffer.writeln('  }');
    buffer.writeln();

    // getAll with cache-first
    buffer.writeln('  @override');
    buffer.writeln('  Future<Result<List<$entityName>, AppFailure>> getAll() async {');
    buffer.writeln('    try {');
    buffer.writeln('      // Try remote first');
    buffer.writeln('      final remote = await remoteDataSource.getAll();');
    buffer.writeln('      // Cache all items');
    buffer.writeln('      for (final item in remote) {');
    buffer.writeln('        await localDataSource.update(item);');
    buffer.writeln('      }');
    buffer.writeln('      return Success(remote);');
    buffer.writeln('    } catch (e) {');
    buffer.writeln('      // Network failed, try cache');
    buffer.writeln('      try {');
    buffer.writeln('        final cached = await localDataSource.getAll();');
    buffer.writeln('        return Success(cached);');
    buffer.writeln('      } catch (cacheError) {');
    buffer.writeln('        return Failure(NetworkFailure(e.toString()));');
    buffer.writeln('      }');
    buffer.writeln('    }');
    buffer.writeln('  }');
    buffer.writeln();

    // create
    buffer.writeln('  @override');
    buffer.writeln('  Future<Result<$entityName, AppFailure>> create($entityName entity) async {');
    buffer.writeln('    try {');
    buffer.writeln('      final created = await remoteDataSource.create(entity);');
    buffer.writeln('      await localDataSource.create(created);');
    buffer.writeln('      return Success(created);');
    buffer.writeln('    } catch (e) {');
    buffer.writeln('      return Failure(NetworkFailure(e.toString()));');
    buffer.writeln('    }');
    buffer.writeln('  }');
    buffer.writeln();

    // update
    buffer.writeln('  @override');
    buffer.writeln('  Future<Result<$entityName, AppFailure>> update($entityName entity) async {');
    buffer.writeln('    try {');
    buffer.writeln('      final updated = await remoteDataSource.update(entity);');
    buffer.writeln('      await localDataSource.update(updated);');
    buffer.writeln('      return Success(updated);');
    buffer.writeln('    } catch (e) {');
    buffer.writeln('      return Failure(NetworkFailure(e.toString()));');
    buffer.writeln('    }');
    buffer.writeln('  }');
    buffer.writeln();

    // delete
    buffer.writeln('  @override');
    buffer.writeln('  Future<Result<void, AppFailure>> delete(String id) async {');
    buffer.writeln('    try {');
    buffer.writeln('      await remoteDataSource.delete(id);');
    buffer.writeln('      await localDataSource.delete(id);');
    buffer.writeln('      return const Success(null);');
    buffer.writeln('    } catch (e) {');
    buffer.writeln('      return Failure(NetworkFailure(e.toString()));');
    buffer.writeln('    }');
    buffer.writeln('  }');
    buffer.writeln('}');

    return buffer.toString();
  }

  /// Get file paths for repository
  Map<String, String> getFilePaths(String entityName) {
    final snake = _toSnakeCase(entityName);

    return {
      'lib/src/domain/repositories/${snake}_repository.dart': 'interface',
      'lib/src/data/repositories/data_${snake}_repository.dart': 'implementation',
    };
  }

  /// Convert PascalCase to snake_case
  String _toSnakeCase(String input) {
    return input
        .replaceAllMapped(
          RegExp(r'[A-Z]'),
          (match) => '_${match.group(0)!.toLowerCase()}',
        )
        .substring(1);
  }
}

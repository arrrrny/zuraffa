import 'json_parser.dart';

/// Generates test files for repositories
///
/// Tests:
/// - Cache-first logic (remote → cache → fallback)
/// - Error handling (network failures)
/// - All CRUD operations
class RepositoryTestGenerator {
  /// Generate repository test
  String generateRepositoryTest(String entityName, String packageName, EntitySchema schema) {
    final className = 'Data${entityName}Repository';
    final buffer = StringBuffer();

    buffer.writeln('// Generated by Zuraffa on ${DateTime.now().toIso8601String()}');
    buffer.writeln('// DO NOT EDIT - Regenerate with: zuraffa generate $entityName');
    buffer.writeln();

    buffer.writeln("import 'package:test/test.dart';");
    buffer.writeln("import 'package:mocktail/mocktail.dart';");
    buffer.writeln("import 'package:zuraffa/zuraffa.dart';");
    buffer.writeln("import 'package:$packageName/src/domain/entities/${_toSnakeCase(entityName)}.dart';");
    buffer.writeln("import 'package:$packageName/src/domain/usecases/${_toSnakeCase(entityName)}_filter.dart';");
    buffer.writeln("import 'package:$packageName/src/data/datasources/${_toSnakeCase(entityName)}_datasource.dart';");
    buffer.writeln("import 'package:$packageName/src/data/repositories/data_${_toSnakeCase(entityName)}_repository.dart';");
    buffer.writeln();

    buffer.writeln("class Mock${entityName}DataSource extends Mock implements ${entityName}DataSource {}");
    buffer.writeln();

    buffer.writeln("void main() {");
    buffer.writeln("  group('$className', () {");
    buffer.writeln("    late Mock${entityName}DataSource mockRemoteDataSource;");
    buffer.writeln("    late Mock${entityName}DataSource mockLocalDataSource;");
    buffer.writeln("    late $className repository;");
    buffer.writeln();

    buffer.writeln("    setUp(() {");
    buffer.writeln("      mockRemoteDataSource = Mock${entityName}DataSource();");
    buffer.writeln("      mockLocalDataSource = Mock${entityName}DataSource();");
    buffer.writeln("      repository = $className(");
    buffer.writeln("        remoteDataSource: mockRemoteDataSource,");
    buffer.writeln("        localDataSource: mockLocalDataSource,");
    buffer.writeln("      );");
    buffer.writeln("    });");
    buffer.writeln();

    // Test getById - success from remote
    final sampleJson = _generateSampleJson(schema);
    buffer.writeln("    group('getById', () {");
    buffer.writeln("      test('returns Success from remote and caches', () async {");
    buffer.writeln("        final entity = $entityName.fromJson($sampleJson);");
    buffer.writeln("        when(() => mockRemoteDataSource.getById(any()))");
    buffer.writeln("            .thenAnswer((_) async => entity);");
    buffer.writeln("        when(() => mockLocalDataSource.update(any()))");
    buffer.writeln("            .thenAnswer((_) async => entity);");
    buffer.writeln();
    buffer.writeln("        final result = await repository.getById('test-1');");
    buffer.writeln();
    buffer.writeln("        expect(result, isA<Success<$entityName, AppFailure>>());");
    buffer.writeln("        result.fold(");
    buffer.writeln("          (failure) => fail('Should not fail'),");
    buffer.writeln("          (data) => expect(data.id, 'test-1'),");
    buffer.writeln("        );");
    buffer.writeln("        verify(() => mockRemoteDataSource.getById('test-1')).called(1);");
    buffer.writeln("        verify(() => mockLocalDataSource.update(entity)).called(1);");
    buffer.writeln("      });");
    buffer.writeln();

    // Test getById - fallback to cache
    buffer.writeln("      test('falls back to cache on network failure', () async {");
    buffer.writeln("        final cachedEntity = $entityName.fromJson($sampleJson);");
    buffer.writeln("        when(() => mockRemoteDataSource.getById(any()))");
    buffer.writeln("            .thenThrow(Exception('Network error'));");
    buffer.writeln("        when(() => mockLocalDataSource.getById(any()))");
    buffer.writeln("            .thenAnswer((_) async => cachedEntity);");
    buffer.writeln();
    buffer.writeln("        final result = await repository.getById('test-1');");
    buffer.writeln();
    buffer.writeln("        expect(result, isA<Success<$entityName, AppFailure>>());");
    buffer.writeln("        result.fold(");
    buffer.writeln("          (failure) => fail('Should not fail with cache'),");
    buffer.writeln("          (data) => expect(data.id, 'test-1'),");
    buffer.writeln("        );");
    buffer.writeln("      });");
    buffer.writeln();

    // Test getById - complete failure
    buffer.writeln("      test('returns Failure when both remote and cache fail', () async {");
    buffer.writeln("        when(() => mockRemoteDataSource.getById(any()))");
    buffer.writeln("            .thenThrow(Exception('Network error'));");
    buffer.writeln("        when(() => mockLocalDataSource.getById(any()))");
    buffer.writeln("            .thenThrow(Exception('Cache error'));");
    buffer.writeln();
    buffer.writeln("        final result = await repository.getById('test-1');");
    buffer.writeln();
    buffer.writeln("        expect(result, isA<Failure<$entityName, AppFailure>>());");
    buffer.writeln("      });");
    buffer.writeln("    });");
    buffer.writeln();

    // Test getAll
    final sampleJson2 = _generateSampleJson(schema, idSuffix: '2');
    buffer.writeln("    group('getAll', () {");
    buffer.writeln("      test('returns Success from remote and caches all items', () async {");
    buffer.writeln("        final entities = [");
    buffer.writeln("          $entityName.fromJson($sampleJson),");
    buffer.writeln("          $entityName.fromJson($sampleJson2),");
    buffer.writeln("        ];");
    buffer.writeln("        final filter = ${entityName}Filter();");
    buffer.writeln("        when(() => mockRemoteDataSource.getAll())");
    buffer.writeln("            .thenAnswer((_) async => entities);");
    buffer.writeln("        when(() => mockLocalDataSource.update(any()))");
    buffer.writeln("            .thenAnswer((_) async => entities[0]);");
    buffer.writeln();
    buffer.writeln("        final result = await repository.getAll(filter);");
    buffer.writeln();
    buffer.writeln("        expect(result, isA<Success<List<$entityName>, AppFailure>>());");
    buffer.writeln("        result.fold(");
    buffer.writeln("          (failure) => fail('Should not fail'),");
    buffer.writeln("          (data) => expect(data.length, 2),");
    buffer.writeln("        );");
    buffer.writeln("        verify(() => mockLocalDataSource.update(any())).called(2);");
    buffer.writeln("      });");
    buffer.writeln();

    buffer.writeln("      test('falls back to cache on network failure', () async {");
    buffer.writeln("        final cachedEntities = [");
    buffer.writeln("          $entityName.fromJson($sampleJson),");
    buffer.writeln("        ];");
    buffer.writeln("        final filter = ${entityName}Filter();");
    buffer.writeln("        when(() => mockRemoteDataSource.getAll())");
    buffer.writeln("            .thenThrow(Exception('Network error'));");
    buffer.writeln("        when(() => mockLocalDataSource.getAll())");
    buffer.writeln("            .thenAnswer((_) async => cachedEntities);");
    buffer.writeln();
    buffer.writeln("        final result = await repository.getAll(filter);");
    buffer.writeln();
    buffer.writeln("        expect(result, isA<Success<List<$entityName>, AppFailure>>());");
    buffer.writeln("      });");
    buffer.writeln("    });");
    buffer.writeln();

    // Test create
    buffer.writeln("    group('create', () {");
    buffer.writeln("      test('creates in remote and local', () async {");
    buffer.writeln("        final entity = $entityName.fromJson($sampleJson);");
    buffer.writeln("        when(() => mockRemoteDataSource.create(any()))");
    buffer.writeln("            .thenAnswer((_) async => entity);");
    buffer.writeln("        when(() => mockLocalDataSource.create(any()))");
    buffer.writeln("            .thenAnswer((_) async => entity);");
    buffer.writeln();
    buffer.writeln("        final result = await repository.create(entity);");
    buffer.writeln();
    buffer.writeln("        expect(result, isA<Success<$entityName, AppFailure>>());");
    buffer.writeln("        verify(() => mockRemoteDataSource.create(entity)).called(1);");
    buffer.writeln("        verify(() => mockLocalDataSource.create(entity)).called(1);");
    buffer.writeln("      });");
    buffer.writeln("    });");
    buffer.writeln();

    // Test update
    buffer.writeln("    group('update', () {");
    buffer.writeln("      test('updates in remote and local', () async {");
    buffer.writeln("        final entity = $entityName.fromJson($sampleJson);");
    buffer.writeln("        when(() => mockRemoteDataSource.update(any()))");
    buffer.writeln("            .thenAnswer((_) async => entity);");
    buffer.writeln("        when(() => mockLocalDataSource.update(any()))");
    buffer.writeln("            .thenAnswer((_) async => entity);");
    buffer.writeln();
    buffer.writeln("        final result = await repository.update(entity);");
    buffer.writeln();
    buffer.writeln("        expect(result, isA<Success<$entityName, AppFailure>>());");
    buffer.writeln("        verify(() => mockRemoteDataSource.update(entity)).called(1);");
    buffer.writeln("        verify(() => mockLocalDataSource.update(entity)).called(1);");
    buffer.writeln("      });");
    buffer.writeln("    });");
    buffer.writeln();

    // Test delete
    buffer.writeln("    group('delete', () {");
    buffer.writeln("      test('deletes from remote and local', () async {");
    buffer.writeln("        when(() => mockRemoteDataSource.delete(any()))");
    buffer.writeln("            .thenAnswer((_) async => null);");
    buffer.writeln("        when(() => mockLocalDataSource.delete(any()))");
    buffer.writeln("            .thenAnswer((_) async => null);");
    buffer.writeln();
    buffer.writeln("        final result = await repository.delete('test-1');");
    buffer.writeln();
    buffer.writeln("        expect(result, isA<Success<void, AppFailure>>());");
    buffer.writeln("        verify(() => mockRemoteDataSource.delete('test-1')).called(1);");
    buffer.writeln("        verify(() => mockLocalDataSource.delete('test-1')).called(1);");
    buffer.writeln("      });");
    buffer.writeln("    });");

    buffer.writeln("  });");
    buffer.writeln("}");

    return buffer.toString();
  }

  /// Get file path for repository test
  String getFilePath(String entityName) {
    return 'test/data/repositories/data_${_toSnakeCase(entityName)}_repository_test.dart';
  }

  /// Generate sample JSON for testing based on EntitySchema
  String _generateSampleJson(EntitySchema schema, {String idSuffix = '1'}) {
    final buffer = StringBuffer();
    buffer.write('{');

    final fields = <String>[];
    for (final field in schema.fields) {
      final value = _getSampleValue(field, schema, idSuffix: idSuffix);
      fields.add("'${field.name}': $value");
    }

    buffer.write(fields.join(', '));
    buffer.write('}');
    return buffer.toString();
  }

  /// Get sample value for field type
  String _getSampleValue(FieldSchema field, EntitySchema parentSchema, {String idSuffix = '1'}) {
    if (field.type.startsWith('List<')) {
      final innerType = field.type.substring(5, field.type.length - 1);
      if (innerType.startsWith(r'$')) {
        // List of nested entities
        final entityName = innerType.substring(1);
        final nestedSchema = parentSchema.nestedEntities.firstWhere(
          (e) => e.name == entityName,
          orElse: () => EntitySchema(name: entityName, fields: []),
        );
        if (nestedSchema.fields.isEmpty) return "[{}]";
        return '[${_generateNestedJson(nestedSchema, idSuffix: idSuffix)}]';
      } else {
        // List of primitives
        return switch (innerType) {
          'String' => "['test$idSuffix']",
          'int' => '[$idSuffix]',
          'double' => '[$idSuffix.5]',
          'bool' => '[true]',
          _ => '[]',
        };
      }
    } else if (field.type.startsWith(r'$')) {
      // Nested entity
      final entityName = field.type.substring(1);
      final nestedSchema = parentSchema.nestedEntities.firstWhere(
        (e) => e.name == entityName,
        orElse: () => EntitySchema(name: entityName, fields: []),
      );
      if (nestedSchema.fields.isEmpty) return "{}";
      return _generateNestedJson(nestedSchema, idSuffix: idSuffix);
    } else {
      // Primitive
      return switch (field.type) {
        'String' => field.name == 'id' ? "'test-$idSuffix'" : "'test ${field.name} $idSuffix'",
        'int' => field.name == 'id' ? idSuffix : '42',
        'double' => '3.14',
        'bool' => 'true',
        _ => 'null',
      };
    }
  }

  /// Generate JSON for nested entity
  String _generateNestedJson(EntitySchema schema, {String idSuffix = '1'}) {
    final buffer = StringBuffer();
    buffer.write('{');

    final fields = <String>[];
    for (final field in schema.fields) {
      final value = _getSampleValue(field, schema, idSuffix: idSuffix);
      fields.add("'${field.name}': $value");
    }

    buffer.write(fields.join(', '));
    buffer.write('}');
    return buffer.toString();
  }

  String _toSnakeCase(String input) {
    return input
        .replaceAllMapped(
          RegExp(r'[A-Z]'),
          (match) => '_${match.group(0)!.toLowerCase()}',
        )
        .substring(1);
  }
}

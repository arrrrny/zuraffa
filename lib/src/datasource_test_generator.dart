/// Generates test files for datasources
///
/// Tests:
/// - Remote datasource with mocked HTTP client
/// - Local datasource with mocked SharedPreferences
/// - Mock datasource basic functionality
class DataSourceTestGenerator {
  /// Generate remote datasource test
  String generateRemoteDataSourceTest(String entityName, String packageName) {
    final className = 'Remote${entityName}DataSource';
    final buffer = StringBuffer();

    buffer.writeln('// Generated by Zuraffa on ${DateTime.now().toIso8601String()}');
    buffer.writeln('// DO NOT EDIT - Regenerate with: zuraffa generate $entityName');
    buffer.writeln();

    buffer.writeln("import 'package:test/test.dart';");
    buffer.writeln("import 'package:mocktail/mocktail.dart';");
    buffer.writeln("import 'package:http/http.dart' as http;");
    buffer.writeln("import 'dart:convert';");
    buffer.writeln("import 'package:$packageName/src/domain/entities/${_toSnakeCase(entityName)}.dart';");
    buffer.writeln("import 'package:$packageName/src/data/datasources/remote_${_toSnakeCase(entityName)}_datasource.dart';");
    buffer.writeln();

    buffer.writeln("class MockHttpClient extends Mock implements http.Client {}");
    buffer.writeln();

    buffer.writeln("void main() {");
    buffer.writeln("  group('$className', () {");
    buffer.writeln("    late MockHttpClient mockClient;");
    buffer.writeln("    late $className datasource;");
    buffer.writeln("    const baseUrl = 'https://api.example.com';");
    buffer.writeln();

    buffer.writeln("    setUp(() {");
    buffer.writeln("      mockClient = MockHttpClient();");
    buffer.writeln("      datasource = $className(client: mockClient, baseUrl: baseUrl);");
    buffer.writeln("    });");
    buffer.writeln();

    // Test getById
    buffer.writeln("    test('getById returns $entityName on success', () async {");
    buffer.writeln("      final json = {'id': 'test-1', 'name': 'Test'};");
    buffer.writeln("      when(() => mockClient.get(any()))");
    buffer.writeln("          .thenAnswer((_) async => http.Response(jsonEncode(json), 200));");
    buffer.writeln();
    buffer.writeln("      final result = await datasource.getById('test-1');");
    buffer.writeln();
    buffer.writeln("      expect(result, isA<$entityName>());");
    buffer.writeln("      expect(result.id, 'test-1');");
    buffer.writeln("    });");
    buffer.writeln();

    // Test getById error
    buffer.writeln("    test('getById throws on error', () async {");
    buffer.writeln("      when(() => mockClient.get(any()))");
    buffer.writeln("          .thenAnswer((_) async => http.Response('Not Found', 404));");
    buffer.writeln();
    buffer.writeln("      expect(() => datasource.getById('test-1'), throwsException);");
    buffer.writeln("    });");
    buffer.writeln();

    // Test getAll
    buffer.writeln("    test('getAll returns list of ${entityName}s', () async {");
    buffer.writeln("      final json = [");
    buffer.writeln("        {'id': 'test-1', 'name': 'Test 1'},");
    buffer.writeln("        {'id': 'test-2', 'name': 'Test 2'},");
    buffer.writeln("      ];");
    buffer.writeln("      when(() => mockClient.get(any()))");
    buffer.writeln("          .thenAnswer((_) async => http.Response(jsonEncode(json), 200));");
    buffer.writeln();
    buffer.writeln("      final result = await datasource.getAll();");
    buffer.writeln();
    buffer.writeln("      expect(result, isA<List<$entityName>>());");
    buffer.writeln("      expect(result.length, 2);");
    buffer.writeln("    });");
    buffer.writeln();

    // Test create
    buffer.writeln("    test('create returns created $entityName', () async {");
    buffer.writeln("      final inputJson = {'id': 'test-1', 'name': 'New'};");
    buffer.writeln("      final entity = $entityName.fromJson(inputJson);");
    buffer.writeln("      when(() => mockClient.post(any(), headers: any(named: 'headers'), body: any(named: 'body')))");
    buffer.writeln("          .thenAnswer((_) async => http.Response(jsonEncode(inputJson), 201));");
    buffer.writeln();
    buffer.writeln("      final result = await datasource.create(entity);");
    buffer.writeln();
    buffer.writeln("      expect(result, isA<$entityName>());");
    buffer.writeln("      expect(result.id, 'test-1');");
    buffer.writeln("    });");

    buffer.writeln("  });");
    buffer.writeln("}");

    return buffer.toString();
  }

  /// Generate local datasource test
  String generateLocalDataSourceTest(String entityName, String packageName) {
    final className = 'Local${entityName}DataSource';
    final buffer = StringBuffer();

    buffer.writeln('// Generated by Zuraffa on ${DateTime.now().toIso8601String()}');
    buffer.writeln('// DO NOT EDIT - Regenerate with: zuraffa generate $entityName');
    buffer.writeln();

    buffer.writeln("import 'package:test/test.dart';");
    buffer.writeln("import 'package:mocktail/mocktail.dart';");
    buffer.writeln("import 'package:shared_preferences/shared_preferences.dart';");
    buffer.writeln("import 'dart:convert';");
    buffer.writeln("import 'package:$packageName/src/domain/entities/${_toSnakeCase(entityName)}.dart';");
    buffer.writeln("import 'package:$packageName/src/data/datasources/local_${_toSnakeCase(entityName)}_datasource.dart';");
    buffer.writeln();

    buffer.writeln("class MockSharedPreferences extends Mock implements SharedPreferences {}");
    buffer.writeln();

    buffer.writeln("void main() {");
    buffer.writeln("  group('$className', () {");
    buffer.writeln("    late MockSharedPreferences mockPrefs;");
    buffer.writeln("    late $className datasource;");
    buffer.writeln();

    buffer.writeln("    setUp(() {");
    buffer.writeln("      mockPrefs = MockSharedPreferences();");
    buffer.writeln("      datasource = $className(sharedPreferences: mockPrefs);");
    buffer.writeln("    });");
    buffer.writeln();

    // Test getAll with data
    buffer.writeln("    test('getAll returns cached ${entityName}s', () async {");
    buffer.writeln("      final json = [");
    buffer.writeln("        {'id': 'test-1', 'name': 'Test 1'},");
    buffer.writeln("        {'id': 'test-2', 'name': 'Test 2'},");
    buffer.writeln("      ];");
    buffer.writeln("      when(() => mockPrefs.getString(any()))");
    buffer.writeln("          .thenReturn(jsonEncode(json));");
    buffer.writeln();
    buffer.writeln("      final result = await datasource.getAll();");
    buffer.writeln();
    buffer.writeln("      expect(result, isA<List<$entityName>>());");
    buffer.writeln("      expect(result.length, 2);");
    buffer.writeln("    });");
    buffer.writeln();

    // Test getAll with no data
    buffer.writeln("    test('getAll returns empty list when no cache', () async {");
    buffer.writeln("      when(() => mockPrefs.getString(any())).thenReturn(null);");
    buffer.writeln();
    buffer.writeln("      final result = await datasource.getAll();");
    buffer.writeln();
    buffer.writeln("      expect(result, isEmpty);");
    buffer.writeln("    });");
    buffer.writeln();

    // Test update
    buffer.writeln("    test('update saves $entityName to cache', () async {");
    buffer.writeln("      final entity = $entityName.fromJson({'id': 'test-1', 'name': 'Test'});");
    buffer.writeln("      when(() => mockPrefs.getString(any())).thenReturn('[]');");
    buffer.writeln("      when(() => mockPrefs.setString(any(), any()))");
    buffer.writeln("          .thenAnswer((_) async => true);");
    buffer.writeln();
    buffer.writeln("      final result = await datasource.update(entity);");
    buffer.writeln();
    buffer.writeln("      expect(result, equals(entity));");
    buffer.writeln("      verify(() => mockPrefs.setString(any(), any())).called(1);");
    buffer.writeln("    });");

    buffer.writeln("  });");
    buffer.writeln("}");

    return buffer.toString();
  }

  /// Generate mock datasource test
  String generateMockDataSourceTest(String entityName, String packageName) {
    final className = 'Mock${entityName}DataSource';
    final buffer = StringBuffer();

    buffer.writeln('// Generated by Zuraffa on ${DateTime.now().toIso8601String()}');
    buffer.writeln('// DO NOT EDIT - Regenerate with: zuraffa generate $entityName');
    buffer.writeln();

    buffer.writeln("import 'package:test/test.dart';");
    buffer.writeln("import 'package:$packageName/src/domain/entities/${_toSnakeCase(entityName)}.dart';");
    buffer.writeln("import 'package:$packageName/src/data/datasources/mock_${_toSnakeCase(entityName)}_datasource.dart';");
    buffer.writeln();

    buffer.writeln("void main() {");
    buffer.writeln("  group('$className', () {");
    buffer.writeln("    late $className datasource;");
    buffer.writeln();

    buffer.writeln("    setUp(() {");
    buffer.writeln("      datasource = $className();");
    buffer.writeln("    });");
    buffer.writeln();

    buffer.writeln("    test('create and getById work correctly', () async {");
    buffer.writeln("      final entity = $entityName.fromJson({'id': 'test-1', 'name': 'Test'});");
    buffer.writeln();
    buffer.writeln("      await datasource.create(entity);");
    buffer.writeln("      final result = await datasource.getById('test-1');");
    buffer.writeln();
    buffer.writeln("      expect(result.id, 'test-1');");
    buffer.writeln("    });");
    buffer.writeln();

    buffer.writeln("    test('getAll returns all items', () async {");
    buffer.writeln("      final entity1 = $entityName.fromJson({'id': 'test-1', 'name': 'Test 1'});");
    buffer.writeln("      final entity2 = $entityName.fromJson({'id': 'test-2', 'name': 'Test 2'});");
    buffer.writeln();
    buffer.writeln("      await datasource.create(entity1);");
    buffer.writeln("      await datasource.create(entity2);");
    buffer.writeln("      final result = await datasource.getAll();");
    buffer.writeln();
    buffer.writeln("      expect(result.length, 2);");
    buffer.writeln("    });");

    buffer.writeln("  });");
    buffer.writeln("}");

    return buffer.toString();
  }

  /// Get file paths for datasource tests
  Map<String, String> getFilePaths(String entityName) {
    final snake = _toSnakeCase(entityName);
    return {
      'test/data/datasources/remote_${snake}_datasource_test.dart': 'remote',
      'test/data/datasources/local_${snake}_datasource_test.dart': 'local',
      'test/data/datasources/mock_${snake}_datasource_test.dart': 'mock',
    };
  }

  String _toSnakeCase(String input) {
    return input
        .replaceAllMapped(
          RegExp(r'[A-Z]'),
          (match) => '_${match.group(0)!.toLowerCase()}',
        )
        .substring(1);
  }
}

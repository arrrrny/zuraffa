import 'json_parser.dart';

/// Generates UseCase classes from entities
///
/// Follows Clean Architecture:
/// - Single responsibility
/// - Depends on repository abstraction
/// - Returns Result<T, Failure>
class UseCaseGenerator {
  /// Generate UseCase file content
  String generateUseCase({
    required String entityName,
    required UseCaseType type,
    String? customName,
  }) {
    final useCaseName = customName ?? _inferUseCaseName(entityName, type);
    final methodName = _inferMethodName(type);
    final repositoryName = '${entityName}Repository';
    final returnType = _getReturnType(entityName, type);
    final paramsType = _getParamsType(entityName, type);
    final buffer = StringBuffer();

    // Header comment
    buffer.writeln('// Generated by Zuraffa on ${DateTime.now().toIso8601String()}');
    buffer.writeln('// DO NOT EDIT - Regenerate with: zuraffa generate $entityName');
    buffer.writeln();

    // Imports
    buffer.writeln("import 'package:zuraffa/zuraffa.dart';");
    buffer.writeln("import '../entities/${_toSnakeCase(entityName)}.dart';");
    buffer.writeln("import '../repositories/${_toSnakeCase(entityName)}_repository.dart';");
    buffer.writeln();

    // Class declaration
    final baseClass = type == UseCaseType.getAll
        ? 'NoParamsUseCase<$returnType>'
        : 'UseCase<$returnType, $paramsType>';

    buffer.writeln('class $useCaseName extends $baseClass {');
    buffer.writeln('  final $repositoryName repository;');
    buffer.writeln();
    buffer.writeln('  $useCaseName(this.repository);');
    buffer.writeln();

    // Execute method
    buffer.writeln('  @override');
    buffer.writeln('  Future<Result<$returnType, AppFailure>> execute(${_getExecuteParams(entityName, type)}) async {');
    buffer.writeln('    return await repository.$methodName(${_getRepositoryArgs(type)});');
    buffer.writeln('  }');
    buffer.writeln('}');

    return buffer.toString();
  }

  /// Get file path for usecase
  String getFilePath(String entityName, UseCaseType type, {String? customName}) {
    final useCaseName = customName ?? _inferUseCaseName(entityName, type);
    return 'lib/src/domain/usecases/${_toSnakeCase(useCaseName)}.dart';
  }

  /// Infer use case name from entity and type
  String _inferUseCaseName(String entityName, UseCaseType type) {
    return switch (type) {
      UseCaseType.get => 'Get${entityName}UseCase',
      UseCaseType.getAll => 'GetAll${entityName}sUseCase',
      UseCaseType.create => 'Create${entityName}UseCase',
      UseCaseType.update => 'Update${entityName}UseCase',
      UseCaseType.delete => 'Delete${entityName}UseCase',
    };
  }

  /// Infer repository method name from type
  String _inferMethodName(UseCaseType type) {
    return switch (type) {
      UseCaseType.get => 'getById',
      UseCaseType.getAll => 'getAll',
      UseCaseType.create => 'create',
      UseCaseType.update => 'update',
      UseCaseType.delete => 'delete',
    };
  }

  /// Get use case base class
  String _getUseCaseBase(String entityName, UseCaseType type) {
    final returnType = _getReturnType(entityName, type);
    final paramsType = _getParamsType(entityName, type);

    return type == UseCaseType.getAll
        ? 'NoParamsUseCase<$returnType>'
        : 'UseCase<$returnType, $paramsType>';
  }

  /// Get return type for use case
  String _getReturnType(String entityName, UseCaseType type) {
    return switch (type) {
      UseCaseType.get => entityName,
      UseCaseType.getAll => 'List<$entityName>',
      UseCaseType.create => entityName,
      UseCaseType.update => entityName,
      UseCaseType.delete => 'void',
    };
  }

  /// Get params type for use case
  String _getParamsType(String entityName, UseCaseType type) {
    return switch (type) {
      UseCaseType.get => 'String',
      UseCaseType.getAll => 'NoParams',
      UseCaseType.create => entityName,
      UseCaseType.update => entityName,
      UseCaseType.delete => 'String',
    };
  }

  /// Get parameters for execute method
  String _getExecuteParams(String entityName, UseCaseType type) {
    return switch (type) {
      UseCaseType.get => 'String id',
      UseCaseType.getAll => 'NoParams params',
      UseCaseType.create => '$entityName entity',
      UseCaseType.update => '$entityName entity',
      UseCaseType.delete => 'String id',
    };
  }

  /// Get repository method arguments
  String _getRepositoryArgs(UseCaseType type) {
    return switch (type) {
      UseCaseType.get => 'id',
      UseCaseType.getAll => '',
      UseCaseType.create => 'entity',
      UseCaseType.update => 'entity',
      UseCaseType.delete => 'id',
    };
  }

  /// Convert PascalCase to snake_case
  String _toSnakeCase(String input) {
    return input
        .replaceAllMapped(
          RegExp(r'[A-Z]'),
          (match) => '_${match.group(0)!.toLowerCase()}',
        )
        .substring(1);
  }
}

/// Types of use cases to generate
enum UseCaseType {
  get,
  getAll,
  create,
  update,
  delete,
}

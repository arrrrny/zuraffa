/// Generates repository interfaces for the domain layer.
///
/// Builds abstract repository classes with method definitions derived from
/// use cases and data requirements.
///
/// Example:
/// ```dart
/// final generator = RepositoryInterfaceGenerator(
///   outputDir: 'lib/src',
///   options: const GeneratorOptions(force: true),
/// );
/// final file = await generator.generate(GeneratorConfig(name: 'Product'));
/// ```
class RepositoryInterfaceGenerator {
  final String outputDir;
  final GeneratorOptions options;
  final AppendExecutor appendExecutor;
  final SpecLibrary specLibrary;

  RepositoryInterfaceGenerator({
    required this.outputDir,
    this.options = const GeneratorOptions(),
    AppendExecutor? appendExecutor,
    SpecLibrary? specLibrary,
  }) : appendExecutor = appendExecutor ?? AppendExecutor(),
       specLibrary = specLibrary ?? const SpecLibrary();

  Future<GeneratedFile> generate(GeneratorConfig config) async {
    final repoName = '${config.name}Repository';
    final fileName = '${config.nameSnake}_repository.dart';
    final filePath = '$outputDir/domain/repositories/$fileName';

    final methods = _buildMethods(config);
    final importPaths = _buildImportPaths(config);
    final clazz = Class(
      (b) => b
        ..name = repoName
        ..abstract = true
        ..methods.addAll(methods),
    );

    final output = specLibrary.emitLibrary(
      specLibrary.library(
        specs: [clazz],
        directives: importPaths.toSet().map(Directive.import),
      ),
      leadingComment:
          '// Generated by zfa for: ${config.name}\n// zfa generate ${config.name} --methods=${config.methods.join(',')} --repository',
    );

    if (File(filePath).existsSync() && !config.force) {
      final existing = await File(filePath).readAsString();

      if (config.revert) {
        if (!config.appendToExisting) {
          return FileUtils.writeFile(
            filePath,
            output,
            'repository',
            force: true,
            dryRun: options.dryRun,
            verbose: options.verbose,
            revert: true,
            skipRevertIfExisted: true,
          );
        }

        final reverted = _removeMethods(
          source: existing,
          className: repoName,
          methods: methods,
        );

        if (const AstHelper().isClassEmpty(reverted, repoName)) {
          return FileUtils.deleteFile(
            filePath,
            'repository',
            dryRun: options.dryRun,
            verbose: options.verbose,
          );
        }

        return FileUtils.writeFile(
          filePath,
          reverted,
          'repository',
          force: true,
          dryRun: options.dryRun,
          verbose: options.verbose,
          revert: false,
        );
      }

      final importLines = _buildImportLines(importPaths);
      final mergedImports = _mergeImports(existing, importLines);
      final appended = _appendMethods(
        source: mergedImports,
        className: repoName,
        methods: methods,
      );
      return FileUtils.writeFile(
        filePath,
        appended,
        'repository',
        force: true,
        dryRun: options.dryRun,
        verbose: options.verbose,
        revert: false,
      );
    }

    if (config.revert) {
      return FileUtils.deleteFile(
        filePath,
        'repository',
        dryRun: options.dryRun,
        verbose: options.verbose,
      );
    }

    return FileUtils.writeFile(
      filePath,
      output,
      'repository',
      force: options.force,
      dryRun: options.dryRun,
      verbose: options.verbose,
      revert: false,
    );
  }

  String _removeMethods({
    required String source,
    required String className,
    required List<Method> methods,
  }) {
    var updated = source;
    final helper = const AstHelper();
    for (final method in methods) {
      final methodName = method.name!;
      updated = helper.removeMethodFromClass(
        source: updated,
        className: className,
        methodName: methodName,
      );
    }
    return updated;
  }

  List<String> _buildImportPaths(GeneratorConfig config) {
    final needsZuraffaImport =
        config.methods.any(
          (method) =>
              method == 'getList' ||
              method == 'update' ||
              method == 'delete' ||
              method == 'watchList',
        ) ||
        config.generateInit;

    final imports = <String>[];
    if (needsZuraffaImport) {
      imports.add('package:zuraffa/zuraffa.dart');
    }
    final entityName = config.name;
    final entitySnake = config.nameSnake;
    final baseImport = PackageUtils.getBaseImport(outputDir);

    if (EntityAnalyzer.isEnum(entityName, outputDir)) {
      imports.add('$baseImport/domain/entities/enums/index.dart');
    } else {
      imports.add('$baseImport/domain/entities/$entitySnake/$entitySnake.dart');
    }
    return imports;
  }

  List<Method> _buildMethods(GeneratorConfig config) {
    final methods = <Method>[];
    if (config.generateInit) {
      methods.add(
        Method(
          (b) => b
            ..name = 'isInitialized'
            ..type = MethodType.getter
            ..returns = refer('Stream<bool>'),
        ),
      );
      methods.add(
        Method(
          (b) => b
            ..name = 'initialize'
            ..returns = refer('Future<void>')
            ..requiredParameters.add(
              Parameter(
                (p) => p
                  ..name = 'params'
                  ..type = refer('InitializationParams'),
              ),
            ),
        ),
      );
      methods.add(
        Method(
          (b) => b
            ..name = 'dispose'
            ..returns = refer('Future<void>'),
        ),
      );
    }

    for (final method in config.methods) {
      switch (method) {
        case 'get':
          methods.add(
            _buildMethod(
              name: 'get',
              returnType: 'Future<${config.name}>',
              paramsType: 'QueryParams<${config.name}>',
              paramsName: 'params',
            ),
          );
          break;
        case 'getList':
          methods.add(
            _buildMethod(
              name: 'getList',
              returnType: 'Future<List<${config.name}>>',
              paramsType: 'ListQueryParams<${config.name}>',
              paramsName: 'params',
            ),
          );
          break;
        case 'create':
          methods.add(
            _buildMethod(
              name: 'create',
              returnType: 'Future<${config.name}>',
              paramsType: config.name,
              paramsName: config.nameCamel,
            ),
          );
          break;
        case 'update':
          // Use Patch for entity-based updates by default
          final dataType = '${config.name}Patch';
          methods.add(
            _buildMethod(
              name: 'update',
              returnType: 'Future<${config.name}>',
              paramsType: 'UpdateParams<${config.idFieldType}, $dataType>',
              paramsName: 'params',
            ),
          );
          break;
        case 'delete':
          methods.add(
            _buildMethod(
              name: 'delete',
              returnType: 'Future<void>',
              paramsType: 'DeleteParams<${config.idFieldType}>',
              paramsName: 'params',
            ),
          );
          break;
        case 'watch':
          methods.add(
            _buildMethod(
              name: 'watch',
              returnType: 'Stream<${config.name}>',
              paramsType: 'QueryParams<${config.name}>',
              paramsName: 'params',
            ),
          );
          break;
        case 'watchList':
          methods.add(
            _buildMethod(
              name: 'watchList',
              returnType: 'Stream<List<${config.name}>>',
              paramsType: 'ListQueryParams<${config.name}>',
              paramsName: 'params',
            ),
          );
          break;
      }
    }
    return methods;
  }

  Method _buildMethod({
    required String name,
    required String returnType,
    required String paramsType,
    required String paramsName,
  }) {
    return Method(
      (b) => b
        ..name = name
        ..returns = refer(returnType)
        ..requiredParameters.add(
          Parameter(
            (p) => p
              ..name = paramsName
              ..type = refer(paramsType),
          ),
        ),
    );
  }

  String _appendMethods({
    required String source,
    required String className,
    required List<Method> methods,
  }) {
    var updated = source;
    for (final method in methods) {
      final methodSource = _emitMethod(method);
      final result = appendExecutor.execute(
        AppendRequest.method(
          source: updated,
          className: className,
          memberSource: methodSource,
        ),
      );
      updated = result.source;
    }
    return updated;
  }

  String _mergeImports(String source, List<String> imports) {
    var updated = source;
    for (final importLine in imports) {
      if (!updated.contains(importLine)) {
        updated = '$importLine\n$updated';
      }
    }
    return updated;
  }

  List<String> _buildImportLines(List<String> importPaths) {
    return importPaths.map((path) => "import '$path';").toList();
  }

  String _emitMethod(Method method) {
    final emitter = DartEmitter(
      orderDirectives: true,
      useNullSafetySyntax: true,
    );
    return method.accept(emitter).toString();
  }
}

import 'json_parser.dart';

/// Generates DataSource classes (Remote/Local/Mock)
///
/// Follows the pattern: RemoteProductDataSource, LocalProductDataSource, MockProductDataSource
class DataSourceGenerator {
  /// Generate DataSource abstract interface
  String generateDataSourceInterface(String entityName) {
    final buffer = StringBuffer();

    buffer.writeln('// Generated by Zuraffa on ${DateTime.now().toIso8601String()}');
    buffer.writeln('// DO NOT EDIT - Regenerate with: zuraffa generate $entityName');
    buffer.writeln();

    buffer.writeln("import '../../domain/entities/${_toSnakeCase(entityName)}.dart';");
    buffer.writeln();

    buffer.writeln('/// Abstract DataSource for $entityName');
    buffer.writeln('abstract class ${entityName}DataSource {');
    buffer.writeln('  /// Get $entityName by ID');
    buffer.writeln('  Future<$entityName> getById(String id);');
    buffer.writeln();
    buffer.writeln('  /// Get all ${entityName}s');
    buffer.writeln('  Future<List<$entityName>> getAll();');
    buffer.writeln();
    buffer.writeln('  /// Create $entityName');
    buffer.writeln('  Future<$entityName> create($entityName entity);');
    buffer.writeln();
    buffer.writeln('  /// Update $entityName');
    buffer.writeln('  Future<$entityName> update($entityName entity);');
    buffer.writeln();
    buffer.writeln('  /// Delete $entityName');
    buffer.writeln('  Future<void> delete(String id);');
    buffer.writeln('}');

    return buffer.toString();
  }

  /// Generate Remote DataSource (API calls)
  String generateRemoteDataSource(String entityName) {
    final className = 'Remote${entityName}DataSource';
    final buffer = StringBuffer();

    buffer.writeln('// Generated by Zuraffa on ${DateTime.now().toIso8601String()}');
    buffer.writeln('// DO NOT EDIT - Regenerate with: zuraffa generate $entityName');
    buffer.writeln();

    buffer.writeln("import 'dart:convert';");
    buffer.writeln("import 'package:http/http.dart' as http;");
    buffer.writeln("import '../../domain/entities/${_toSnakeCase(entityName)}.dart';");
    buffer.writeln("import '${_toSnakeCase(entityName)}_datasource.dart';");
    buffer.writeln();

    buffer.writeln('class $className implements ${entityName}DataSource {');
    buffer.writeln('  final http.Client client;');
    buffer.writeln('  final String baseUrl;');
    buffer.writeln();
    buffer.writeln('  $className({required this.client, required this.baseUrl});');
    buffer.writeln();

    // getById
    buffer.writeln('  @override');
    buffer.writeln('  Future<$entityName> getById(String id) async {');
    buffer.writeln("    final response = await client.get(Uri.parse('\$baseUrl/${_toSnakeCase(entityName)}s/\$id'));");
    buffer.writeln();
    buffer.writeln('    if (response.statusCode == 200) {');
    buffer.writeln('      final json = jsonDecode(response.body) as Map<String, dynamic>;');
    buffer.writeln('      return ${entityName}.fromJson(json);');
    buffer.writeln('    } else {');
    buffer.writeln("      throw Exception('Failed to load $entityName: \${response.statusCode}');");
    buffer.writeln('    }');
    buffer.writeln('  }');
    buffer.writeln();

    // getAll
    buffer.writeln('  @override');
    buffer.writeln('  Future<List<$entityName>> getAll() async {');
    buffer.writeln("    final response = await client.get(Uri.parse('\$baseUrl/${_toSnakeCase(entityName)}s'));");
    buffer.writeln();
    buffer.writeln('    if (response.statusCode == 200) {');
    buffer.writeln('      final jsonList = jsonDecode(response.body) as List;');
    buffer.writeln('      return jsonList.map((json) => ${entityName}.fromJson(json as Map<String, dynamic>)).toList();');
    buffer.writeln('    } else {');
    buffer.writeln("      throw Exception('Failed to load ${entityName}s: \${response.statusCode}');");
    buffer.writeln('    }');
    buffer.writeln('  }');
    buffer.writeln();

    // create
    buffer.writeln('  @override');
    buffer.writeln('  Future<$entityName> create($entityName entity) async {');
    buffer.writeln("    final response = await client.post(");
    buffer.writeln("      Uri.parse('\$baseUrl/${_toSnakeCase(entityName)}s'),");
    buffer.writeln("      headers: {'Content-Type': 'application/json'},");
    buffer.writeln('      body: jsonEncode(entity.toJson()),');
    buffer.writeln('    );');
    buffer.writeln();
    buffer.writeln('    if (response.statusCode == 201) {');
    buffer.writeln('      final json = jsonDecode(response.body) as Map<String, dynamic>;');
    buffer.writeln('      return ${entityName}.fromJson(json);');
    buffer.writeln('    } else {');
    buffer.writeln("      throw Exception('Failed to create $entityName: \${response.statusCode}');");
    buffer.writeln('    }');
    buffer.writeln('  }');
    buffer.writeln();

    // update
    buffer.writeln('  @override');
    buffer.writeln('  Future<$entityName> update($entityName entity) async {');
    buffer.writeln("    final response = await client.put(");
    buffer.writeln("      Uri.parse('\$baseUrl/${_toSnakeCase(entityName)}s/\${entity.id}'),");
    buffer.writeln("      headers: {'Content-Type': 'application/json'},");
    buffer.writeln('      body: jsonEncode(entity.toJson()),');
    buffer.writeln('    );');
    buffer.writeln();
    buffer.writeln('    if (response.statusCode == 200) {');
    buffer.writeln('      final json = jsonDecode(response.body) as Map<String, dynamic>;');
    buffer.writeln('      return ${entityName}.fromJson(json);');
    buffer.writeln('    } else {');
    buffer.writeln("      throw Exception('Failed to update $entityName: \${response.statusCode}');");
    buffer.writeln('    }');
    buffer.writeln('  }');
    buffer.writeln();

    // delete
    buffer.writeln('  @override');
    buffer.writeln('  Future<void> delete(String id) async {');
    buffer.writeln("    final response = await client.delete(Uri.parse('\$baseUrl/${_toSnakeCase(entityName)}s/\$id'));");
    buffer.writeln();
    buffer.writeln('    if (response.statusCode != 204) {');
    buffer.writeln("      throw Exception('Failed to delete $entityName: \${response.statusCode}');");
    buffer.writeln('    }');
    buffer.writeln('  }');
    buffer.writeln('}');

    return buffer.toString();
  }

  /// Generate Local DataSource (SharedPreferences/Hive)
  String generateLocalDataSource(String entityName) {
    final className = 'Local${entityName}DataSource';
    final buffer = StringBuffer();

    buffer.writeln('// Generated by Zuraffa on ${DateTime.now().toIso8601String()}');
    buffer.writeln('// DO NOT EDIT - Regenerate with: zuraffa generate $entityName');
    buffer.writeln();

    buffer.writeln("import 'dart:convert';");
    buffer.writeln("import 'package:shared_preferences/shared_preferences.dart';");
    buffer.writeln("import '../../domain/entities/${_toSnakeCase(entityName)}.dart';");
    buffer.writeln("import '${_toSnakeCase(entityName)}_datasource.dart';");
    buffer.writeln();

    buffer.writeln('class $className implements ${entityName}DataSource {');
    buffer.writeln('  final SharedPreferences sharedPreferences;');
    buffer.writeln("  static const String _cacheKey = '${_toSnakeCase(entityName)}_cache';");
    buffer.writeln();
    buffer.writeln('  $className({required this.sharedPreferences});');
    buffer.writeln();

    buffer.writeln('  @override');
    buffer.writeln('  Future<$entityName> getById(String id) async {');
    buffer.writeln('    final all = await getAll();');
    buffer.writeln('    return all.firstWhere((e) => e.id == id);');
    buffer.writeln('  }');
    buffer.writeln();

    buffer.writeln('  @override');
    buffer.writeln('  Future<List<$entityName>> getAll() async {');
    buffer.writeln('    final jsonString = sharedPreferences.getString(_cacheKey);');
    buffer.writeln('    if (jsonString != null) {');
    buffer.writeln('      final jsonList = jsonDecode(jsonString) as List;');
    buffer.writeln('      return jsonList.map((json) => ${entityName}.fromJson(json as Map<String, dynamic>)).toList();');
    buffer.writeln('    }');
    buffer.writeln('    return [];');
    buffer.writeln('  }');
    buffer.writeln();

    buffer.writeln('  @override');
    buffer.writeln('  Future<$entityName> create($entityName entity) async {');
    buffer.writeln('    final all = await getAll();');
    buffer.writeln('    all.add(entity);');
    buffer.writeln('    await _saveAll(all);');
    buffer.writeln('    return entity;');
    buffer.writeln('  }');
    buffer.writeln();

    buffer.writeln('  @override');
    buffer.writeln('  Future<$entityName> update($entityName entity) async {');
    buffer.writeln('    final all = await getAll();');
    buffer.writeln('    final index = all.indexWhere((e) => e.id == entity.id);');
    buffer.writeln('    if (index != -1) {');
    buffer.writeln('      all[index] = entity;');
    buffer.writeln('      await _saveAll(all);');
    buffer.writeln('    }');
    buffer.writeln('    return entity;');
    buffer.writeln('  }');
    buffer.writeln();

    buffer.writeln('  @override');
    buffer.writeln('  Future<void> delete(String id) async {');
    buffer.writeln('    final all = await getAll();');
    buffer.writeln('    all.removeWhere((e) => e.id == id);');
    buffer.writeln('    await _saveAll(all);');
    buffer.writeln('  }');
    buffer.writeln();

    buffer.writeln('  Future<void> _saveAll(List<$entityName> entities) async {');
    buffer.writeln('    final jsonList = entities.map((e) => e.toJson()).toList();');
    buffer.writeln('    await sharedPreferences.setString(_cacheKey, jsonEncode(jsonList));');
    buffer.writeln('  }');
    buffer.writeln('}');

    return buffer.toString();
  }

  /// Generate Mock DataSource (for testing)
  String generateMockDataSource(String entityName) {
    final className = 'Mock${entityName}DataSource';
    final buffer = StringBuffer();

    buffer.writeln('// Generated by Zuraffa on ${DateTime.now().toIso8601String()}');
    buffer.writeln('// DO NOT EDIT - Regenerate with: zuraffa generate $entityName');
    buffer.writeln();

    buffer.writeln("import '../../domain/entities/${_toSnakeCase(entityName)}.dart';");
    buffer.writeln("import '${_toSnakeCase(entityName)}_datasource.dart';");
    buffer.writeln();

    buffer.writeln('class $className implements ${entityName}DataSource {');
    buffer.writeln('  final List<$entityName> _mockData = [];');
    buffer.writeln();

    buffer.writeln('  @override');
    buffer.writeln('  Future<$entityName> getById(String id) async {');
    buffer.writeln('    return _mockData.firstWhere((e) => e.id == id);');
    buffer.writeln('  }');
    buffer.writeln();

    buffer.writeln('  @override');
    buffer.writeln('  Future<List<$entityName>> getAll() async {');
    buffer.writeln('    return List.from(_mockData);');
    buffer.writeln('  }');
    buffer.writeln();

    buffer.writeln('  @override');
    buffer.writeln('  Future<$entityName> create($entityName entity) async {');
    buffer.writeln('    _mockData.add(entity);');
    buffer.writeln('    return entity;');
    buffer.writeln('  }');
    buffer.writeln();

    buffer.writeln('  @override');
    buffer.writeln('  Future<$entityName> update($entityName entity) async {');
    buffer.writeln('    final index = _mockData.indexWhere((e) => e.id == entity.id);');
    buffer.writeln('    if (index != -1) {');
    buffer.writeln('      _mockData[index] = entity;');
    buffer.writeln('    }');
    buffer.writeln('    return entity;');
    buffer.writeln('  }');
    buffer.writeln();

    buffer.writeln('  @override');
    buffer.writeln('  Future<void> delete(String id) async {');
    buffer.writeln('    _mockData.removeWhere((e) => e.id == id);');
    buffer.writeln('  }');
    buffer.writeln('}');

    return buffer.toString();
  }

  /// Get file paths for all datasources
  Map<String, String> getFilePaths(String entityName) {
    final basePath = 'lib/src/data/datasources';
    final snake = _toSnakeCase(entityName);

    return {
      '$basePath/${snake}_datasource.dart': 'interface',
      '$basePath/remote_${snake}_datasource.dart': 'remote',
      '$basePath/local_${snake}_datasource.dart': 'local',
      '$basePath/mock_${snake}_datasource.dart': 'mock',
    };
  }

  /// Convert PascalCase to snake_case
  String _toSnakeCase(String input) {
    return input
        .replaceAllMapped(
          RegExp(r'[A-Z]'),
          (match) => '_${match.group(0)!.toLowerCase()}',
        )
        .substring(1);
  }
}

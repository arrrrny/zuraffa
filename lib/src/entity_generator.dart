import 'json_parser.dart';

/// Generates Morphy entity files from EntitySchema
///
/// Follows zikzak_morphy conventions:
/// - Abstract class with $ prefix: abstract class $Product
/// - Nested entity references use $: $Customer, List<$OrderItem>
/// - Add @Morphy(generateJson: true) annotation
/// - Enforces Primitive JSON Forever™ philosophy
///
/// See MORPHY_GUIDE.md for best practices
class MorphyEntityGenerator {
  /// Generate entity file content with TLC ❤️
  String generateEntity(EntitySchema schema, {List<String>? referencedEntities, bool isValueObject = false}) {
    // Validate schema follows Primitive JSON Forever™ (skip id check for value objects)
    _validateSchema(schema, isValueObject: isValueObject);

    final buffer = StringBuffer();

    // Header comment with generation info
    buffer.writeln('// Generated by Zuraffa on ${DateTime.now().toIso8601String()}');
    if (isValueObject) {
      buffer.writeln('// DO NOT EDIT - Regenerate with: zuraffa generate ${schema.name} --from-json file.json --value-object');
      buffer.writeln('//');
      buffer.writeln('// Morphy Value Object: Immutable, Type-safe, JSON-serializable');
      buffer.writeln('// No identity (no id field) - defined by its attributes');
    } else {
      buffer.writeln('// DO NOT EDIT - Regenerate with: zuraffa create entity ${schema.name}');
      buffer.writeln('//');
      buffer.writeln('// Morphy Entity: Immutable, Type-safe, JSON-serializable');
      buffer.writeln('// Has identity (id field) - tracked and cached');
    }
    buffer.writeln('// See: https://pub.dev/packages/zikzak_morphy');
    buffer.writeln();

    // Import morphy annotation
    buffer.writeln("import 'package:zikzak_morphy/zikzak_morphy.dart';");

    // Import referenced entities (other Morphy entities)
    if (referencedEntities != null && referencedEntities.isNotEmpty) {
      buffer.writeln();
      buffer.writeln('// Referenced entities');
      for (final entityName in referencedEntities) {
        final fileName = _toSnakeCase(entityName);
        buffer.writeln("import '$fileName.dart';");
      }
    }

    buffer.writeln();

    // Part directives for generated files
    final fileName = _toSnakeCase(schema.name);
    buffer.writeln("part '$fileName.morphy.dart';");
    buffer.writeln("part '$fileName.g.dart';");
    buffer.writeln();

    // Annotation with generateJson enabled
    if (isValueObject) {
      buffer.writeln('/// ${schema.name} value object');
      buffer.writeln('///');
      buffer.writeln('/// Generated from JSON with Zuraffa + zikzak_morphy');
      buffer.writeln('/// - Immutable by default');
      buffer.writeln('/// - Type-safe');
      buffer.writeln('/// - JSON serialization included (fromJson/toJson)');
      buffer.writeln('/// - Equality and hashCode generated');
      buffer.writeln('/// - copyWith() for immutable updates');
      buffer.writeln('/// - No identity (value objects are defined by their attributes)');
    } else {
      buffer.writeln('/// ${schema.name} entity');
      buffer.writeln('///');
      buffer.writeln('/// Generated from JSON with Zuraffa + zikzak_morphy');
      buffer.writeln('/// - Immutable by default');
      buffer.writeln('/// - Type-safe');
      buffer.writeln('/// - JSON serialization included (fromJson/toJson)');
      buffer.writeln('/// - Equality and hashCode generated');
      buffer.writeln('/// - copyWith() for immutable updates');
      buffer.writeln('/// - Has identity (entities have id for tracking)');
    }
    buffer.writeln('@Morphy(generateJson: true)');

    // Class declaration with $ prefix
    buffer.writeln('abstract class \$${schema.name} {');

    // Generate fields with documentation
    for (final field in schema.fields) {
      final fieldType = _getMorphyFieldType(field);
      final nullable = field.isNullable ? '?' : '';

      // Add field comment
      buffer.writeln('  /// ${_getFieldDescription(field)}');
      buffer.writeln('  $fieldType$nullable get ${field.name};');

      if (field != schema.fields.last) {
        buffer.writeln();
      }
    }

    buffer.writeln('}');

    return buffer.toString();
  }

  /// Validate schema follows Primitive JSON Forever™ philosophy
  void _validateSchema(EntitySchema schema, {bool isValueObject = false}) {
    // Check for required 'id' field (only for entities, not value objects)
    if (!isValueObject) {
      // Accept both String and int for id
      final hasId = schema.fields.any((f) => f.name == 'id' && (f.type == 'String' || f.type == 'int'));
      if (!hasId) {
        throw Exception(
          '❌ Entity ${schema.name} must have an id field (String or int).\n\n'
          'Zuraffa requires all entities to have an identifier for:\n'
          '  - Cache management in repositories\n'
          '  - Data synchronization\n'
          '  - Testing and mocking\n\n'
          'Add an "id" field to your JSON:\n'
          '  {"id": "unique-id", ...rest of fields}     // String id\n'
          '  {"id": 123, ...rest of fields}              // int id\n\n'
          'Or use --value-object flag if this is a Value Object:\n'
          '  zuraffa generate ${schema.name} --from-json file.json --value-object\n\n'
          'See MORPHY_GUIDE.md for more details.',
        );
      }
    }

    // Check for unsupported types
    for (final field in schema.fields) {
      _validateFieldType(schema.name, field);
    }
  }

  /// Validate field type is supported by Primitive JSON Forever™
  void _validateFieldType(String entityName, FieldSchema field) {
    const primitives = ['String', 'int', 'double', 'bool', 'DateTime', 'dynamic'];

    final baseType = field.type.startsWith('List<')
        ? _extractListType(field.type)
        : field.type;

    // Check if it's a primitive or entity reference
    final isPrimitive = primitives.contains(baseType);
    final isEntity = baseType.startsWith(r'$') || !primitives.contains(baseType);

    if (!isPrimitive && !isEntity) {
      throw Exception(
        '❌ Unsupported type "${field.type}" for field "${field.name}" in $entityName.\n\n'
        'Zuraffa follows Primitive JSON Forever™ philosophy.\n'
        'Supported types:\n'
        '  ✅ String, int, double, bool, DateTime\n'
        '  ✅ List<T> (where T is any supported type)\n'
        '  ✅ Entity references (e.g., Customer, Product)\n\n'
        '❌ Not supported:\n'
        '  Map<K, V> with non-String keys\n'
        '  Complex nested structures\n'
        '  Union types\n\n'
        'See MORPHY_GUIDE.md for alternatives.',
      );
    }
  }

  /// Get field description for documentation
  String _getFieldDescription(FieldSchema field) {
    if (field.name == 'id') return 'Unique identifier (required by Zuraffa)';
    if (field.name.endsWith('At')) return 'Timestamp';
    if (field.name.endsWith('Url')) return 'URL string';
    if (field.name.endsWith('Id')) return 'Foreign key reference';
    if (field.name.startsWith('is')) return 'Boolean flag';
    if (field.name.startsWith('has')) return 'Boolean flag';
    if (field.type.startsWith('List<')) {
      final inner = _extractListType(field.type);
      return 'List of $inner items';
    }
    return field.name.replaceAllMapped(
      RegExp(r'([A-Z])'),
      (match) => ' ${match.group(1)!.toLowerCase()}',
    ).trim();
  }

  /// Get the correct Morphy field type with $ references
  String _getMorphyFieldType(FieldSchema field) {
    // Primitive types - no $ prefix
    const primitives = ['String', 'int', 'double', 'bool', 'DateTime', 'dynamic'];

    if (primitives.contains(field.type)) {
      return field.type;
    }

    // List types
    if (field.type.startsWith('List<')) {
      final innerType = _extractListType(field.type);

      // List of primitives - no $ prefix
      if (primitives.contains(innerType)) {
        return field.type; // List<String>, List<int>, etc.
      }

      // List of entities - ADD $ to inner type
      return 'List<\$$innerType>'; // List<$Product>
    }

    // Nested entity - ADD $ prefix
    return '\$${field.type}'; // $Customer
  }

  /// Extract inner type from List<T>
  String _extractListType(String listType) {
    final match = RegExp(r'List<(.+)>').firstMatch(listType);
    return match?.group(1) ?? 'dynamic';
  }

  /// Generate file path for entity
  String getFilePath(String entityName) {
    return 'lib/src/domain/entities/${_toSnakeCase(entityName)}.dart';
  }

  /// Convert PascalCase to snake_case
  String _toSnakeCase(String input) {
    return input.replaceAllMapped(
      RegExp(r'[A-Z]'),
      (match) => '_${match.group(0)!.toLowerCase()}',
    ).substring(1); // Remove leading underscore
  }

  /// Extract entity references from schema fields
  List<String> _extractReferencedEntities(EntitySchema schema) {
    final referenced = <String>{};

    for (final field in schema.fields) {
      // Skip primitives
      const primitives = ['String', 'int', 'double', 'bool', 'DateTime', 'dynamic'];

      if (primitives.contains(field.type)) continue;

      // Handle List<Entity>
      if (field.type.startsWith('List<')) {
        final innerType = _extractListType(field.type);
        if (!primitives.contains(innerType)) {
          referenced.add(innerType);
        }
      } else {
        // Direct entity reference
        referenced.add(field.type);
      }
    }

    return referenced.toList();
  }

  /// Generate all entity files (main + nested)
  ///
  /// For nested entities:
  /// - If parent is a Value Object (isValueObject=true), all nested are Value Objects
  /// - If parent is an Entity (isValueObject=false), nested auto-detect based on id field
  /// - Auto-detection: Has id field → Entity, No id field → Value Object
  Map<String, String> generateAllEntities(EntitySchema schema, {bool isValueObject = false}) {
    final files = <String, String>{};

    // Generate main entity with references
    final referencedEntities = _extractReferencedEntities(schema);
    files[getFilePath(schema.name)] = generateEntity(schema, referencedEntities: referencedEntities, isValueObject: isValueObject);

    // Generate nested entities recursively with auto-detection
    for (final nested in schema.nestedEntities) {
      // Auto-detect for nested entities: check if they have id field
      final hasIdField = nested.fields.any((f) => f.name == 'id' && (f.type == 'String' || f.type == 'int'));

      // If parent is Value Object, all nested are Value Objects
      // Otherwise, auto-detect based on id field presence
      final shouldBeValueObject = isValueObject || !hasIdField;

      files.addAll(generateAllEntities(nested, isValueObject: shouldBeValueObject));
    }

    return files;
  }
}

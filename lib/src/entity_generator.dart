import 'json_parser.dart';

/// Generates Morphy entity files from EntitySchema
///
/// Follows zikzak_morphy conventions:
/// - Abstract class with $ prefix: abstract class $Product
/// - Nested entity references use $: $Customer, List<$OrderItem>
/// - Add @Morphy(generateJson: true) annotation
class MorphyEntityGenerator {
  /// Generate entity file content
  String generateEntity(EntitySchema schema) {
    final buffer = StringBuffer();

    // Header comment
    buffer.writeln('// Generated by Zuraffa on ${DateTime.now().toIso8601String()}');
    buffer.writeln('// DO NOT EDIT - Regenerate with: zuraffa create entity ${schema.name}');
    buffer.writeln();

    // Annotation
    buffer.writeln('@morphy');
    buffer.writeln('@Morphy(generateJson: true)');

    // Class declaration with $ prefix
    buffer.writeln('abstract class \$${schema.name} {');

    // Fields
    for (final field in schema.fields) {
      final fieldType = _getMorphyFieldType(field);
      final nullable = field.isNullable ? '?' : '';
      buffer.writeln('  $fieldType$nullable get ${field.name};');
    }

    buffer.writeln('}');

    return buffer.toString();
  }

  /// Get the correct Morphy field type with $ references
  String _getMorphyFieldType(FieldSchema field) {
    // Primitive types - no $ prefix
    const primitives = ['String', 'int', 'double', 'bool', 'DateTime', 'dynamic'];

    if (primitives.contains(field.type)) {
      return field.type;
    }

    // List types
    if (field.type.startsWith('List<')) {
      final innerType = _extractListType(field.type);

      // List of primitives - no $ prefix
      if (primitives.contains(innerType)) {
        return field.type; // List<String>, List<int>, etc.
      }

      // List of entities - ADD $ to inner type
      return 'List<\$$innerType>'; // List<$Product>
    }

    // Nested entity - ADD $ prefix
    return '\$${field.type}'; // $Customer
  }

  /// Extract inner type from List<T>
  String _extractListType(String listType) {
    final match = RegExp(r'List<(.+)>').firstMatch(listType);
    return match?.group(1) ?? 'dynamic';
  }

  /// Generate file path for entity
  String getFilePath(String entityName) {
    return 'lib/domain/entities/${_toSnakeCase(entityName)}.dart';
  }

  /// Convert PascalCase to snake_case
  String _toSnakeCase(String input) {
    return input.replaceAllMapped(
      RegExp(r'[A-Z]'),
      (match) => '_${match.group(0)!.toLowerCase()}',
    ).substring(1); // Remove leading underscore
  }

  /// Generate all entity files (main + nested)
  Map<String, String> generateAllEntities(EntitySchema schema) {
    final files = <String, String>{};

    // Generate main entity
    files[getFilePath(schema.name)] = generateEntity(schema);

    // Generate nested entities recursively
    for (final nested in schema.nestedEntities) {
      files.addAll(generateAllEntities(nested));
    }

    return files;
  }
}
